package com.ironmovies.models;

public class SimilarMovie {
    private int id;
    private String title;

    public SimilarMovie() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
